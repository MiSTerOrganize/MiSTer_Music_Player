// Read logic
wire [6:0] idx = (char_code >= 7'h20 && char_code <= 7'h7F) ? (char_code - 7'h20) : 7'd0;
reg [23:0] char_data;

always @(posedge clk) begin
    char_data <= font_data[idx];
end

// Extract row: row 0 = bits [23:20], row 1 = [19:16], ..., row 5 = [3:0]
// row N = bits [(5-N)*4 +: 4]
assign pixels = char_data[(3'd5 - row) * 4 +: 4];

