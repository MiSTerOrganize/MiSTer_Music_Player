//============================================================================
//
//  Music Player — 4x6 Bitmap Font ROM
//
//  96 printable ASCII characters (0x20 to 0x7F).
//  Each character: 4 pixels wide, 6 pixels tall.
//  Stored as 24 bits per character (6 rows x 4 columns).
//  Row 0 is top, bit 3 is leftmost pixel.
//
//  Usage: font_rom[char_index] gives 24 bits.
//    Row N = bits [(5-N)*4 +: 4], where bit 3 = left pixel.
//
//  Total: 96 * 24 bits = 96 entries in a 24-bit wide BRAM.
//  Fits easily in one M10K block.
//
//  Copyright (C) 2026 MiSTer Organize -- GPL-3.0
//
//============================================================================

